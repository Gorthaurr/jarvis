/**
 * Текстовый wake word «Джарвис» (§3).
 *
 * Акустического движка нет (MockWakeWord), но речь и так распознаётся (Deepgram),
 * поэтому будим по тексту: вне активного разговора реагируем ТОЛЬКО на обращение
 * «Джарвис» (с допуском на варианты распознавания). Чистые функции — тестируются.
 */

/** Ядро: варианты, как STT может расслышать «Джарвис» (рус/лат). Границы — Unicode. */
const CORE = "(?<![\\p{L}])(?:джарвис|джарвес|джарвиз|джервис|жарвис|ярвис|jarvis)(?![\\p{L}])";
const WAKE_RE = new RegExp(CORE, "iu");
/** То же + прилегающая пунктуация/пробелы (чтобы вырезать «Джарвис,» целиком). */
const WAKE_STRIP_RE = new RegExp(`[\\s,.!?:;—-]*${CORE}[\\s,.!?:;—-]*`, "iu");

/** Есть ли в реплике обращение «Джарвис». */
export function isWakeAddressed(text: string): boolean {
  return WAKE_RE.test(text);
}

/** Убрать обращение «Джарвис» (с прилегающей пунктуацией), оставив команду. */
export function stripWake(text: string): string {
  return text.replace(WAKE_STRIP_RE, " ").replace(/\s+/gu, " ").trim();
}
