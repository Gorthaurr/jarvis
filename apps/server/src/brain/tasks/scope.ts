/**
 * Классификатор «правка текущей задачи vs новая отдельная задача» (§20, параллельность).
 *
 * Применяется ТОЛЬКО когда уже выполняется фоновая задача и пришла НОВАЯ содержательная
 * реплика (не команда управления — это отдельный classifyTaskControl). Цель — различить:
 *  - «edit»: реплика правит/дополняет то, что Джарвис сейчас делает («добавь раздел»,
 *    «сделай подробнее», «убери последнее») → продолжаем в контексте текущей задачи;
 *  - «new»: это ОТДЕЛЬНОЕ дело («а ещё закажи такси», «открой почту») → запускаем новую
 *    параллельную задачу со СВОИМ чистым контекстом, чтобы она не путалась с текущей.
 *
 * Корень жалобы: сейчас новая реплика во время задачи всегда тянет контекст текущей → «вторую
 * обособленную задачу не дать». Поэтому по умолчанию (без явных правочных маркеров) → «new».
 * Чистая детерминированная функция (как classifyTaskControl) — без LLM/сети/состояния.
 */

import { foldText, latinToCyrillic } from "@jarvis/shared";

/** Решение о области реплики относительно активной задачи. */
export type TaskScope = "edit" | "new";

/** Нормализация для матча по словам (единый `foldText` из @jarvis/shared, окаймление пробелами). */
const normalize = (text: string): string => foldText(text, { pad: true });

/**
 * Явные маркеры ПРАВКИ текущего результата (модификация того, что уже делается). Консервативно:
 * только слова, которые трудно спутать с самостоятельным делом. Неоднозначные («ещё», «также»)
 * НЕ включаем — они часто значат «вдобавок ОТДЕЛЬНО».
 */
const EDIT_STEMS = [
  "добавь", "добавить", "допиши", "дополни", "дополнить", "расширь", "расширить",
  "сократи", "укороти", "удлини", "измени", "изменить", "исправь", "исправить",
  "поправь", "поправить", "переделай", "переделать", "замени", "заменить",
  // «короче» убрано — это массовый филлер («короче, закажи такси» — это НОВАЯ задача, не правка).
  "переименуй", "детальн", "подробн", "длинн", "побольше", "поменьше",
] as const;

/**
 * Маркеры-ссылки на ТЕКУЩИЙ объект. Голые «в этом/в эту/в этот» убраны — это частотные
 * обстоятельства («в этом городе найди отель» — новая задача), давали ложный edit. Оставлены
 * формы, явно указывающие на объект правки.
 */
const EDIT_REFERENCES = [
  "туда", "сюда", "там же", "в тот же", "в ту же",
  "к нему", "к ней", "к этому", "в него", "в нее", "его подробнее", "это подробнее",
  // Специфичные формы «в этот <объект>» — это правка текущего артефакта (НЕ голое «в этом году»).
  "в этот документ", "в эту таблицу", "в этот файл", "в этот текст", "в это письмо",
  "в этот список", "в этот отчет", "в этот реферат", "в эту презентацию",
] as const;

/**
 * Маркеры ОТКАЗА/РЕДИРЕКТА текущего хода («делаешь не то, переиграй») — сильный сигнал ПРАВКИ во время
 * задачи. Применяется только при активной задаче, поэтому «не то/не так» тут почти наверняка = «не туда
 * пошёл», а не самостоятельное дело. Бареное «нет/иначе/лучше» НЕ включаем — слишком частотны вне правки.
 */
const REJECT_MARKERS = [
  "не то", "не так", "не это", "не туда", "не такой", "не такую", "не такое",
  "не правильно", "неправильно", "вместо", "по другому", "не нужно так",
] as const;

function hasStem(norm: string, stems: readonly string[]): boolean {
  return stems.some((s) => norm.includes(` ${s}`));
}
// norm окаймлён пробелами → ` фраза ` матчит по границам слов (не подстрокой внутри слова).
function hasPhrase(norm: string, phrases: readonly string[]): boolean {
  return phrases.some((p) => norm.includes(` ${p} `));
}

/**
 * Классифицировать область реплики относительно активной задачи (§20). Чистая функция.
 * Есть явный маркер правки/ссылка на текущий объект → «edit»; иначе → «new» (отдельная задача).
 */
export function classifyTaskScope(text: string): TaskScope {
  const norm = normalize(text);
  if (norm === "") return "new";
  if (hasStem(norm, EDIT_STEMS)) return "edit";
  if (hasPhrase(norm, EDIT_REFERENCES)) return "edit";
  if (hasPhrase(norm, REJECT_MARKERS)) return "edit"; // «не то / не так / вместо» → правка текущего
  return "new";
}

// ── Дубль-гейт (§20, аудит 2026-07-02; усилен по эпизоду 2026-07-10) ────────────────────────────
// Живой случай 1: «продолжи видео на ютубе» и через 6 секунд «продолжу видео на ютубе» (STT-вариация /
// нетерпеливый повтор) породили ДВЕ параллельные задачи — обе пришлось отменять, «остановил» ×2.
// Живой случай 2 (2026-07-10): повтор «запусти поиск в доте» распознан ОБРЫВКОМ ЛАТИНИЦЕЙ «в dot'е.» —
// Жаккар 0.17 → вторая петля, обе убиты потолком, $1.09 впустую. Жаккар МАТЕМАТИЧЕСКИ не ловит
// фрагмент-подмножество (даже идеальное «в доте» даёт 2/4=0.5) → добавлен overlap coefficient
// (пересечение/короткая сторона) + канонизация латиницы (dot'е→доте) через latinToCyrillic.

/** Грубый стем для сравнения словоформ: у слов ≥5 букв срезаем окончание до общего корня. */
function stemToken(t: string): string {
  return t.length >= 5 ? t.slice(0, Math.max(4, t.length - 2)) : t;
}

/**
 * ПОЛНЫЕ токены фразы (без стемминга): апострофы убираем ДО foldText (иначе «dot'е» разорвётся на
 * «dot»+«е»), латиницу токена канонизируем в кириллицу primary-рендерингом (dot'е→доте) + ё→е
 * (foldText уже свернул ё исходника, а транслит мог вернуть «ё» диграфом yo).
 */
function tokenSet(text: string): Set<string> {
  return new Set(
    normalize(String(text ?? "").replace(/['’ʼ`]/gu, ""))
      .split(/\s+/u)
      .filter(Boolean)
      .map((t) => (/[a-z]/i.test(t) ? latinToCyrillic(t).replace(/ё/g, "е") : t)),
  );
}

/**
 * Почти та же ли фраза, что цель активной задачи (повтор / STT-вариация / STT-обрывок повтора)?
 * Два теста (любой достаточен), оба при ≥2 токенах с каждой стороны:
 *  • стем-Жаккар ≥ 0.75 (ТОЧНОЕ равенство стемов) — почти дословный повтор со словоформами
 *    («продолжи/продолжу видео на ютубе»: стемы обоих = «продол»);
 *  • фрагмент-overlap по ПОЛНЫМ токенам (пересечение/короткая сторона ≥ 0.8) + минимум один
 *    совпавший СОДЕРЖАТЕЛЬНЫЙ токен (≥4 букв) — фрагмент-повтор («в доте» ⊂ «запусти поиск в доте»).
 *    Именно полные токены, не стемы и не префиксы: адверсариал-ревью 2026-07-10 показало, что
 *    стем/префикс-вложение в короткой стороне даёт массовые ложные дубли («включи свет» ≈ «включи
 *    светлую тему», «проверь почту» ≈ «…он почти готов» — стемы «почт» совпадают, полные токены НЕТ).
 * Чистая функция (юнит-тест).
 */
export function isDuplicateGoal(text: string, activeGoal: string): boolean {
  const a = tokenSet(text);
  const b = tokenSet(activeGoal);
  if (a.size < 2 || b.size < 2) return false; // однословные — слишком фуззи, не рискуем

  // 1) Почти дословный повтор: Жаккар по СТЕМАМ (точное равенство — ловит словоформы продолжи/продолжу).
  const stemsA = new Set([...a].map(stemToken));
  const stemsB = new Set([...b].map(stemToken));
  let stemInter = 0;
  for (const s of stemsA) if (stemsB.has(s)) stemInter += 1;
  const union = new Set([...stemsA, ...stemsB]).size;
  if (union > 0 && stemInter / union >= 0.75) return true;

  // 2) Фрагмент-повтор: overlap по ПОЛНЫМ токенам + хотя бы одно содержательное совпадение.
  let inter = 0;
  let contentHit = false;
  for (const t of a) {
    if (b.has(t)) {
      inter += 1;
      if (t.length >= 4) contentHit = true;
    }
  }
  return contentHit && inter / Math.min(a.size, b.size) >= 0.8;
}
