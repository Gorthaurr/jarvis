import { describe, expect, it } from "vitest";
import { cleanDisfluency } from "./disfluency.js";

describe("cleanDisfluency — причёсывание спонтанной речи (§10)", () => {
  it("удаляет звуки-запинки", () => {
    expect(cleanDisfluency("открой эээ телеграм")).toBe("открой телеграм");
    expect(cleanDisfluency("ммм создай файл")).toBe("создай файл");
    expect(cleanDisfluency("э-э запусти ворд")).toBe("запусти ворд");
  });

  it("удаляет «это самое» как филлер, но не трогает осмысленное «это»", () => {
    expect(cleanDisfluency("создай это самое файл")).toBe("создай файл");
    expect(cleanDisfluency("прочитай это письмо")).toBe("прочитай это письмо");
  });

  it("схлопывает немедленный повтор слова", () => {
    expect(cleanDisfluency("открой открой ютуб")).toBe("открой ютуб");
  });

  it("склеивает оборванное слово с досказанным (префикс)", () => {
    expect(cleanDisfluency("теле-, телеграм")).toBe("телеграм");
    expect(cleanDisfluency("откр открой документ")).toBe("открой документ");
  });

  it("консервативен: короткий префикс без дефиса не съедается", () => {
    expect(cleanDisfluency("по подожди")).toBe("по подожди");
  });

  it("осмысленный префикс без дефиса (далёкий по длине) НЕ съедается — это разные слова", () => {
    // Без дефиса требуем близость длин (хвост ≤2): «план планировщик» — РАЗНЫЕ слова, не заикание.
    expect(cleanDisfluency("план планировщик открой")).toBe("план планировщик открой");
    // А близкий по длине префикс без дефиса — это заикание, схлопываем («откр открой»).
    expect(cleanDisfluency("откр открой")).toBe("открой");
  });

  it("осмысленные слова из стоп-листа НЕ съедаются как обрывок (под подожди)", () => {
    expect(cleanDisfluency("под подожди немного")).toBe("под подожди немного");
    expect(cleanDisfluency("так такси вызови")).toBe("так такси вызови");
  });

  it("«а-а-а»/«э-э-э» (3+ дефисных) съедаются целиком", () => {
    expect(cleanDisfluency("а-а-а открой ютуб")).toBe("открой ютуб");
    expect(cleanDisfluency("э-э-э запусти ворд")).toBe("запусти ворд");
  });

  it("«не X, а Y» НЕ трогаем эвристикой (искажало норму; решает LLM)", () => {
    // Раньше превращалось в «открой ютуб» — теперь фраза идёт в LLM как есть.
    expect(cleanDisfluency("не открывай, а закрой")).toBe("не открывай, а закрой");
  });

  it("чистую фразу не меняет (идемпотентно)", () => {
    expect(cleanDisfluency("открой телеграм")).toBe("открой телеграм");
    expect(cleanDisfluency("создай на рабочем столе файл")).toBe("создай на рабочем столе файл");
  });

  it("«ну» НЕ трогаем (осмысленно/решает lead-strip роутера)", () => {
    expect(cleanDisfluency("ну открой ютуб")).toBe("ну открой ютуб");
  });

  it("фраза-целиком-запинка схлопывается в пусто (вызывающий оставит оригинал)", () => {
    expect(cleanDisfluency("эээ")).toBe("");
    expect(cleanDisfluency("ммм")).toBe("");
  });
});
