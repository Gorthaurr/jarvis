/**
 * Очистка транскрипта от спонтанной речи (§7, §10): запинки, повторы, обрывки слов.
 * STT не умеет фильтрацию филлеров — «пользователь оговорился, но имел в виду X» причёсываем
 * здесь, ДО роутинга, чтобы и tier0, и LLM получали чистую фразу.
 *
 * Принцип: КОНСЕРВАТИВНО. Трогаем только почти наверняка мусор (хезитации, дубли, явные
 * обрывки). Самоисправления «не X, а Y» НЕ трогаем эвристикой (она искажала нормальные фразы)
 * — это надёжно разруливает LLM, толерантный к disfluency. Лучше недочистить, чем исказить.
 */

/**
 * Хезитации-филлеры: звуки запинки + «это самое». ПОРЯДОК альтернатив важен (дефисные/длинные —
 * первыми, иначе «э{1,3}» съест первую «э» из «э-э»). Дефисные формы ПОВТОРЯЕМЫ ((?:э-)+э),
 * чтобы «э-э-э»/«а-а-а» съедались целиком. Одиночные «а»/«м» НЕ трогаем (союз «а», редкое «м»).
 */
const HESITATION_RE =
  /(?<![\p{L}\p{N}])(?:это самое|мда|(?:э-)+э|(?:а-)+а|эм{1,2}|м{2,3}|э{1,3})(?![\p{L}\p{N}])/giu;

/**
 * Короткие слова, которые являются префиксами других слов, но осмысленны сами по себе —
 * их НЕЛЬЗЯ схлопывать как «обрывок» («под подожди», «так такси», «что чтото»). Защита от
 * ложной склейки (баг: «под» съедался перед «подожди»).
 */
const STUTTER_STOPLIST = new Set([
  "под", "над", "при", "про", "так", "там", "тут", "что", "это", "эта", "эти", "для",
  "три", "сто", "его", "как", "сам", "сама", "сами", "вот", "все", "всё", "тот", "том",
  "она", "они", "оно", "мне", "нам", "вам", "наш", "ваш",
]);

/**
 * Причесать транскрипт спонтанной речи. Идемпотентна, безопасна на «чистом» вводе.
 * Порядок: хезитации → обрывки/дубли → схлопнуть пробелы.
 */
export function cleanDisfluency(input: string): string {
  let s = input;
  s = s.replace(HESITATION_RE, " ");
  s = mergeStutters(s);
  return s.replace(/\s{2,}/gu, " ").replace(/\s+([,.!?])/gu, "$1").trim();
}

/**
 * Склейка заиканий на уровне токенов:
 *  - немедленный повтор слова: «открой открой ютуб» → «открой ютуб»;
 *  - обрывок + полное слово, где обрывок — префикс следующего: «теле-, телеграм» → «телеграм»,
 *    «откр открой» → «открой». С дефисом — от 2 симв.; без дефиса — от 4 И не из стоп-листа
 *    (иначе осмысленные «под/так/что…» съедались перед «подожди/такси/…»).
 */
function mergeStutters(text: string): string {
  const tokens = text.split(/\s+/u).filter(Boolean);
  const out: string[] = [];
  const norm = (t: string): string => t.toLowerCase().replace(/[^\p{L}\p{N}]/gu, "");
  for (let i = 0; i < tokens.length; i += 1) {
    const cur = tokens[i]!;
    const next = tokens[i + 1];
    if (next !== undefined) {
      const a = norm(cur);
      const b = norm(next);
      // Немедленный дубль слова — оставляем один.
      if (a.length > 0 && a === b) continue;
      // Обрывок-префикс: дропаем обрывок, оставляем полное слово.
      const endsWithDash = /-+$/u.test(cur);
      const isPrefix = a.length >= 1 && a.length < b.length && b.startsWith(a);
      if (isPrefix && (endsWithDash ? a.length >= 2 : a.length >= 4 && !STUTTER_STOPLIST.has(a))) continue;
    }
    out.push(cur);
  }
  return out.join(" ");
}
