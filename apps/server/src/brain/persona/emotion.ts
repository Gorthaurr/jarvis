/**
 * Распознавание голосовой команды СМЕНЫ ЭМОЦИИ подачи (§11, §21) — детерминированно, без LLM.
 *
 * «говори зло», «скажи радостно», «вырази злость», «с радостной интонацией», «говори обычно» →
 * семантическая эмоция (Emotion) или null. Эмоция персистится в профиле (как режим-маска) и
 * влияет И на голос (роль TTS), И на слова (оверлей в системный промпт). Возврат к норме — «нейтрально».
 *
 * Консервативность: требуется явный маркер «о подаче речи» (говори/скажи/произнеси/вырази/интонация/
 * «по-…ому»), иначе обычные реплики со словом «радость/зло» не перехватываются. Токенизация —
 * кирилл-безопасная (startsWith по началу токена, как в modes.ts: \b/\w в JS не работают с кириллицей).
 */
import type { Emotion } from "../../integrations/tts-emotion.js";

/** Текст команды → эмоция, либо null если это не команда смены подачи. */
export function matchEmotionCommand(text: string): Emotion | null {
  const norm = ` ${text
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[^a-zа-я0-9-]+/giu, " ")
    .replace(/\s+/g, " ")
    .trim()} `;
  const tokens = norm.trim().split(" ").filter(Boolean);
  const hasPhrase = (...ps: string[]): boolean => ps.some((p) => norm.includes(p));
  const hasStem = (...stems: string[]): boolean =>
    tokens.some((tok) => stems.some((s) => tok.startsWith(s)));

  // Маркер «о ПОДАЧЕ речи» — без него «радостную новость» / «злой человек» не трогаем. «будь/стань»
  // достигают сюда только если НЕ совпали с режимом-маской (modes.matchModeCommand проверяется раньше).
  const speechMarker =
    hasStem("говор", "скаж", "произнес", "произнос", "ответ", "вырази", "покаж", "будь", "стань") ||
    hasPhrase("интонац", " тон ", " тоном ") ||
    /\sпо-[а-я]+ому\s/.test(norm); // «по-злому», «по-доброму», «по-строгому»
  if (!speechMarker) return null;

  // Возврат к норме — проверяем ПЕРВЫМ (фраза «говори обычно» содержит маркер говори).
  if (hasPhrase(" обычно ", " обычным ", " как обычно ", " без эмоци", " нейтральн", " спокойн", " ровн")) {
    return "neutral";
  }
  // Корень «зл» ловит зло/злой/злым/злость/злоба/злись (формы с о/ы/а — «злым» ≠ startsWith «зло»).
  if (hasStem("зл", "сердит", "гневн", "раздраж", "ярост", "груб") || hasPhrase("по-злому", " злость", " злостью")) {
    return "angry";
  }
  if (
    hasStem("радост", "весел", "ликующ", "восторж", "жизнерадост", "воодушев") ||
    hasPhrase("по-доброму", " радостью", " радость")
  ) {
    return "happy";
  }
  if (hasStem("строг") || hasPhrase("по-строгому")) return "strict";
  if (hasStem("шепот", "шепч") || hasPhrase("шепотом")) return "whisper";
  return null;
}

/** Человеко-имя эмоции для подтверждения/настроек. */
export function emotionName(emotion: Emotion): string {
  return { neutral: "обычный тон", happy: "радостно", angry: "зло", strict: "строго", whisper: "шёпотом" }[emotion];
}

/**
 * Оверлей к системному промпту: просит LLM подобрать СЛОВА под эмоцию (голос её несёт отдельно,
 * через роль TTS). Без оверлея gold-персона дворецкого отказывалась «злиться» (лог 2026-06-18:
 * «злость не из моего репертуара») — поэтому прямо разрешаем актёрскую задачу по просьбе пользователя.
 */
const EMOTION_OVERLAY: Record<Emotion, string> = {
  neutral: "",
  angry:
    "РЕЖИМ ПОДАЧИ: зло/сердито. Пользователь ПОПРОСИЛ говорить зло — выполни это как актёрскую задачу: " +
    "резче, с раздражением в словах и тоне. НЕ отказывайся «невозмутимостью дворецкого» и не читай мораль — " +
    "просто сыграй злую подачу (без оскорблений и мата).",
  happy:
    "РЕЖИМ ПОДАЧИ: радостно/воодушевлённо. Говори тепло, живо, с явной радостью и лёгким восторгом.",
  strict:
    "РЕЖИМ ПОДАЧИ: строго/сдержанно-внушительно. Говори веско, собранно, без шуток.",
  whisper:
    "РЕЖИМ ПОДАЧИ: тихо, доверительным полушёпотом. Короче и мягче обычного.",
};

/** Текст оверлея эмоции для системного промпта (пусто для нейтральной/неизвестной). */
export function emotionOverlay(emotion: string | undefined): string {
  return EMOTION_OVERLAY[(emotion ?? "neutral") as Emotion] ?? "";
}
