/**
 * Режимы-«маски» Джарвиса (§11) — переключаемый ТОН одного ассистента, НЕ другой персонаж.
 *
 * Идентичность всегда «Джарвис» (см. persona.md). Режим — это оверлей к персоне (доп. инструкция
 * тона) + опциональный голос (voiceId/настройки ElevenLabs). Базовый режим — дворецкий (без
 * оверлея: его задаёт сама persona.md). Переключение — голосом («будь дерзким», «будь собой»)
 * или из настроек. Хранится в профиле (profile.mode), переживает рестарт.
 */

/** Тонкие настройки голоса ElevenLabs под режим (экспрессия/темп). Опционально. */
export interface ModeVoice {
  /** Свой voiceId ElevenLabs (если задан — иначе дефолтный из конфига). */
  voiceId?: string;
  /** voice_settings: 0..1 (style — выразительность), speed (~0.7..1.2). */
  stability?: number;
  style?: number;
  speed?: number;
}

export interface PersonaMode {
  id: string;
  /** Человеко-имя для подтверждения/настроек. */
  name: string;
  /** Оверлей к системному промпту (доп. инструкция тона). Пусто у базового. */
  overlay: string;
  /** Параметры голоса под режим (опционально). */
  voice?: ModeVoice;
}

export const DEFAULT_MODE_ID = "butler";

