/** Режим по id (или базовый, если неизвестен). */
export function getMode(id: string | undefined): PersonaMode {
  return (id && MODES[id]) || MODES[DEFAULT_MODE_ID]!;
}

/** Список режимов для настроек/каталога. */
export function listModes(): PersonaMode[] {
  return Object.values(MODES);
}

// ── Распознавание голосовой команды смены режима (детерминированно, без LLM) ──

/**
 * «будь дерзким», «режим рассказчика», «будь собой» → id режима, иначе null.
 * Консервативно: нужен явный маркер «включения режима» + стем тона — чтобы не перехватывать
 * обычные команды. На подстроках (а не \b/\w — те в JS не работают с кириллицей).
 */
export function matchModeCommand(text: string): string | null {
  // Токенизация с кирилл-границами: стемы матчим по НАЧАЛУ токена, не подстрокой. Иначе «стань»
  // ловилось в «доСТАНЬ», «нагл» в «НАГЛядно», «весел» в «веСЁЛое» → ложная смена режима и
  // проглот команды (в agent при ненулевом modeId — ранний return).
  const norm = ` ${text.toLowerCase().replace(/ё/g, "е").replace(/[^a-zа-я0-9]+/giu, " ").replace(/\s+/g, " ").trim()} `;
  const tokens = norm.trim().split(" ").filter(Boolean);
  const hasPhrase = (...ps: string[]): boolean => ps.some((p) => norm.includes(` ${p} `));
  const hasStem = (...stems: string[]): boolean => tokens.some((tok) => stems.some((s) => tok.startsWith(s)));
  // Возврат к базовому (проверяем первым: «хватит дерзить» содержит стем «дерз»).
  if (hasPhrase("будь собой", "обычный режим", "обычном режиме", "по дворецки", "как обычно", "хватит дерзить", "хватит кривляться")) {
    return "butler";
  }
  // Маркер смены режима — токен начинается с «будь/стань/режим». «включи/говори» убраны: слишком
  // частотны в обычных командах («говори громче», «включи музыку») и глотали их.
  if (!hasStem("будь", "стань", "режим")) return null;
  if (hasStem("дерз", "нагл", "резче", "резко")) return "bold";
  if (hasStem("рассказчик", "сказочн", "театральн")) return "storyteller";
  if (hasStem("юмор", "смешн", "шутник", "весел", "пошути")) return "comedian";
  return null;
}
