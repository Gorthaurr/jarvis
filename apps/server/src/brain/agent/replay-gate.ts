/**
 * §P0 ГЕЙТ АВТО-РЕПЛЕЯ (форензика логов 2026-07-14, docs/LOG_FORENSICS_2026-07-14.md):
 * из 15 слепых авто-реплеев макросов за два дня ~10 запущены ЧУЖОЙ/разговорной речью — recall на
 * шумном e5-small пробивался генерик-навыками (sim 0.82–0.89: мат в Discord → «закрыть приложение»,
 * разговор о базе данных → 14-шаговый макрос «нажал S куда попало»). Реплей = реальные жесты в
 * активное окно БЕЗ LLM-раундов, поэтому вход в него гейтится ЖЁСТЧЕ recall-подсказки:
 *
 *  1) СЕМАНТИЧЕСКАЯ УВЕРЕННОСТЬ, ДВА ПОРОГА: гибридный ранг recallSim ≥ JARVIS_AUTO_REPLAY_MIN_SIM
 *     (деф 0.92) И сырой косинус recallSimRaw ≥ JARVIS_AUTO_REPLAY_MIN_RAW_COS (деф 0.84 — выше
 *     задокументированной полосы шума e5-small «несвязанные набирают 0.82+», ниже живых легитимных
 *     попаданий 0.856+). Один гибридный порог обходился бустами (ревью: лексика ≤0.2 + платформа 0.1
 *     протаскивали rawCos~0.7 — навык ТОЙ ЖЕ платформы, но ДРУГОГО действия получал слепые жесты).
 *     Лексический recall без косинуса (recallSim отсутствует) авто-реплей не получает вовсе.
 *  2) КОМАНДНЫЙ ГЛАГОЛ: реплика без императива действия («я просираюсь от чернослива») — не команда.
 *     Роутерный ACTION_VERB_RE не подходит: он намеренно без «запусти/открой» (те ловятся tier0).
 *  3) НЕ РАЗГОВОР: conversational-ход (вопрос/рассуждение) навык-ПОДСКАЗКУ получает (Б6), но жесты
 *     в GUI на вопрос не реплеятся.
 *  4) ТОЛЬКО ЯВНОЕ ОБРАЩЕНИЕ: реплика, принятая КАТЯЩИМСЯ ОКНОМ разговора без «Джарвис»
 *     (viaWake=false), жестов не получает — именно так чужая речь чаще всего попадает в пайплайн.
 *  5) НЕ МЕТА-НАВЫК: навык ПРО ЗАПИСЬ/СОЗДАНИЕ макросов-навыков («писать надёжный макрос») несёт
 *     клики КОНКРЕТНОГО прошлого прогона — реплеить их на новую реплику категорически неверно.
 *     Игровая лексика («прокачай скиллы», «включи макрос фарма») мета-навыком НЕ считается (ревью).
 *
 * Гейт закрывает ТОЛЬКО слепой авто-реплей прежде петли: recall-подсказка в промпт и явный
 * skill_execute моделью (она решает с контекстом) не трогаются. Чистые функции — прямые юнит-тесты.
 */

/** Порог ГИБРИДНОЙ уверенности (косинус+бонусы) для слепого авто-реплея (строже recall-инжекта 0.82). */
const AUTO_REPLAY_MIN_SIM = (() => {
  const n = Number.parseFloat(process.env.JARVIS_AUTO_REPLAY_MIN_SIM ?? "");
  return Number.isFinite(n) && n > 0 && n <= 1.5 ? n : 0.92;
})();

/** Порог СЫРОГО косинуса (без бустов) — бусты лексики/платформы не заменяют семантику (ревью). */
const AUTO_REPLAY_MIN_RAW_COS = (() => {
  const n = Number.parseFloat(process.env.JARVIS_AUTO_REPLAY_MIN_RAW_COS ?? "");
  return Number.isFinite(n) && n > 0 && n <= 1 ? n : 0.84;
})();

/**
 * Императивы GUI-команд (стемы, `[\p{L}]*` — словоформы кириллицы). Список НАМЕРЕННО шире роутерного
 * ACTION_VERB_RE (тот без «запусти/открой» — их ловит tier0 раньше, а сюда текст доходит целиком).
 * Стем терпит прошедшее время («запустил») — это грубый фильтр «в реплике вообще есть действие»,
 * точность даёт связка с порогом sim, а не сам глагол.
 */
const COMMAND_VERB_RE = new RegExp(
  "(?<![\\p{L}\\p{N}])(?:" +
    "запуст[\\p{L}]*|запуска[\\p{L}]*|откр[оы][\\p{L}]*|закр[оы][\\p{L}]*|включ[\\p{L}]*|выключ[\\p{L}]*|" +
    "вруб[\\p{L}]*|выруб[\\p{L}]*|перезапуст[\\p{L}]*|переключ[\\p{L}]*|найд[\\p{L}]*|поищ[\\p{L}]*|" +
    "нажм[\\p{L}]*|кликн[\\p{L}]*|набер[\\p{L}]*|введ[\\p{L}]*|напечата[\\p{L}]*|напиш[\\p{L}]*|" +
    "отправ[\\p{L}]*|пошл[иё][\\p{L}]*|постав[\\p{L}]*|поставь|останов[\\p{L}]*|прекрат[\\p{L}]*|" +
    "продолж[\\p{L}]*|сверн[\\p{L}]*|разверн[\\p{L}]*|обнов[\\p{L}]*|перейд[\\p{L}]*|зайд[\\p{L}]*|" +
    "выйд[\\p{L}]*|смен[иь][\\p{L}]*|поменя[\\p{L}]*|убер[\\p{L}]*|удал[\\p{L}]*|сохран[\\p{L}]*|" +
    "созда[\\p{L}]*|сдела[\\p{L}]*|покаж[\\p{L}]*|прокача[\\p{L}]*|запиш[\\p{L}]*|скача[\\p{L}]*|" +
    "провер[\\p{L}]*|зафикс[\\p{L}]*|подключ[\\p{L}]*|отключ[\\p{L}]*|" +
    // Ревью (пробелы аллоулиста — легитимные классы команд теряли $0-реплей): добавь/ответь/сыграй/
    // перемотай/вставь/скопируй/выбери/переведи и их словоформы.
    "добав[\\p{L}]*|ответ[\\p{L}]*|сыгра[\\p{L}]*|перемота[\\p{L}]*|встав[\\p{L}]*|скопиру[\\p{L}]*|" +
    "копиру[\\p{L}]*|выбер[\\p{L}]*|выбир[\\p{L}]*|перевед[\\p{L}]*" +
    ")",
  "iu",
);

/** Есть ли в реплике командный глагол — грубый фильтр «это вообще похоже на команду». */
export function looksLikeCommandUtterance(text: string): boolean {
  return COMMAND_VERB_RE.test(text);
}

/** Мета-навык = про ЗАПИСЬ/СОЗДАНИЕ/ОБУЧЕНИЕ макросов-навыков. Матч ФРАЗОВЫЙ (ревью р2 — независимые
 *  подстроки verb∧noun по всему name+when ловили «создать лобби … прокачать скиллы»):
 *  глагол записи/обучения (с границей слова — «писа» ВНУТРИ «описание» не считается; БЕЗ причастий,
 *  вкл. КРАТКИЕ — «записанный/записан/создан макрос» описывают макрос, а не действие записи, ревью р3;
 *  приставочные «перезапиши/допиши/напиши» и глаголы обучения «выучи/запомни/научи» — тоже мета, р3)
 *  + ≤2 слов + мета-слово ПРЯМЫМ объектом (им./вин. падеж: «макрос/навыки/скиллы»; косвенные
 *  «скиллов/макросом» — игровая лексика «билд скиллов», не мета; дефисные компаунды «скилл-шот» —
 *  игровые слова, не мета, р3). Одиночное «макрос/скилл» без глагола записи — штатная игровая речь
 *  владельца («включи макрос фарма», «прокачай скиллы») и мета-навыком НЕ является. Поля проверяются
 *  РАЗДЕЛЬНО (р3): окно фразы не должно пересекать склейку name+when («Записать клип» + when
 *  «макрос OBS настроен» — фраза, которой нет ни в одном поле).
 *  ⚠️ Осознанный трейдофф (р4): игровое «выучить скиллы» лежит ПО МЕТА-СТОРОНЕ границы — оно
 *  неотличимо регекспом от HERMES-меты «выучи новый навык», а carve-out дал бы зеркальный пропуск
 *  реального мета-навыка; цена — LLM-петля вместо $0-реплея у такого имени (честно, в логе). */
const META_SKILL_RE = new RegExp(
  "(?<![\\p{L}\\p{N}])(?:" +
    "запис(?!ан)|запиш|перезапис(?!ан)|перезапиш|напис(?!ан)|напиш|допис(?!ан)|допиш|перепис(?!ан)|перепиш|" +
    // науч/обуч: (?![её]н) — как у сохран (р4: ё-причастия «обучённый/научённый/обучён» — описание
    // макроса, не действие обучения; е-формы уже резались, ё проскакивала).
    "писа(?!н)|пиш|сохран(?![её]н)|созда(?!н)|редактир|выуч(?!ен)|науч(?![её]н)|обуч(?![её]н)|запомн(?!ен)" +
    ")[\\p{L}]*" +
    "(?:\\s+[\\p{L}\\p{N}-]+){0,2}?\\s+(?:макрос|навык|скилл|skill|macro)(?:ы|и|s)?(?![\\p{L}-])",
  "iu",
);

export function isMetaSkill(name: string, when: string): boolean {
  return META_SKILL_RE.test(name) || META_SKILL_RE.test(when);
}

/** Вход гейта: минимум, который известен петле в точке решения об авто-реплее. */
export interface AutoReplayGateInput {
  /** Реплика пользователя (чистый текст хода). */
  text: string;
  /** Recall-нутый навык: имя+когда (мета-детект), гибридная уверенность recallSim (нет = лексический
   *  матч) и сырой косинус recallSimRaw (нет → консервативно берётся recallSim). */
  recalled: { name: string; when: string; recallSim?: number; recallSimRaw?: number };
  /** Разговорный ход (вопрос/рассуждение) — жесты не положены. */
  conversational: boolean;
  /** Реплика принята явным «Джарвис» (или подтверждённым second-chance). false = катящееся окно.
   *  undefined = канал без wake-семантики (dev.text/чат) — считается явным обращением. */
  viaWake?: boolean;
}

/**
 * Причина блокировки слепого авто-реплея, null = реплей разрешён. Строка-причина уходит в лог —
 * разбор «почему макрос не сработал мгновенно» не должен требовать дедукции по коду (закон
 * наблюдаемости; и обратный разбор «почему сработал на чужую речь» тоже).
 */
export function autoReplayBlocked(input: AutoReplayGateInput): string | null {
  if (input.conversational) return "conversational-ход (вопрос/рассуждение) — жесты не реплеим";
  if (input.viaWake === false) return "реплика из окна разговора без «Джарвис» — слепые жесты только по явному обращению";
  if (isMetaSkill(input.recalled.name, input.recalled.when)) return "мета-навык (про запись макросов/навыков) — его шаги суть прошлый прогон";
  const sim = input.recalled.recallSim;
  if (sim === undefined) return "лексический recall без семантической уверенности";
  if (sim < AUTO_REPLAY_MIN_SIM) return `recall sim ${sim.toFixed(3)} < порога авто-реплея ${AUTO_REPLAY_MIN_SIM}`;
  // Сырой косинус: бусты (лексика/платформа) не заменяют семантику. Нет raw (прямой вызов/старый
  // мок) → консервативно проверяем той же планкой сам sim... он уже ≥ 0.92 > raw-порога — т.е.
  // фолбэк мягкий по построению; прод (recallSemantic) отдаёт настоящий raw всегда.
  const raw = input.recalled.recallSimRaw ?? sim;
  if (raw < AUTO_REPLAY_MIN_RAW_COS)
    return `сырой косинус ${raw.toFixed(3)} < порога ${AUTO_REPLAY_MIN_RAW_COS} (бусты лексики/платформы не заменяют семантику)`;
  if (!looksLikeCommandUtterance(input.text)) return "в реплике нет командного глагола — не похоже на команду";
  return null;
}
