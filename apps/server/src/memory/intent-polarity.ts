/**
 * ПОЛЯРНОСТЬ НАМЕРЕНИЯ (§8 recall-гард). Эмбеддинг-косинус не различает «запусти X» и
 * «прекрати X» — фразы почти одинаковы лексически и семантически (живой случай 2026-07-03:
 * «прекрати поиск у доти» → recall навыка «запустить поиск игры в дота 2» с sim 0.856; с
 * авто-макросом реплей ЗАПУСТИЛ бы поиск вместо остановки). Полярность решается
 * детерминированно ДО инжекта/реплея: строгий конфликт start↔stop режет кандидата,
 * смешанные/нейтральные формулировки не трогаем (ложный блок вреднее пропуска — решит модель).
 */

/** Класс полярности намерения по глаголам действия. */
export type IntentPolarity = "start" | "stop" | "mixed" | "neutral";

/**
 * Стемы «запускных» глаголов (матч по началу токена). Только ГЛАГОЛЫ — существительные
 * («игра», «запуск процесса» в описании) дают ложную полярность. «перезапусти» намеренно
 * не ловится (restart ≈ start, конфликт не нужен).
 */
const START_STEMS: readonly string[] = [
  "запуст", // запусти/запустить/запустил
  "включ", // включи/включить
  "начн", // начни/начнём
  "начат", // начать
  "начина", // начинай
  "поставь", // поставь трек («поставь на паузу» уравновесит стем «пауз» → mixed)
  "постав", // поставить
  "откро", // открой
  "откры", // открыть/открываю («откр» ловил бы «откровенно»)
  "сыгра", // сыграй
  "воспроизвед", // воспроизведи
  "продолж", // продолжи (resume ≈ start)
  "возобнов", // возобнови
  "найд", // найди (найди катку = запусти поиск)
  "start",
  "launch",
  "open",
  "play",
  "begin",
  "resume",
  "run",
  "enable",
];

/** Стемы «останавливающих» глаголов (матч по началу токена). */
const STOP_STEMS: readonly string[] = [
  "прекрат", // прекрати/прекратить
  "останов", // останови/остановить
  "отмен", // отмени/отменить
  "стоп",
  "выключ", // выключи
  "выруб", // выруби
  "заверш", // заверши
  "прерв", // прерви
  "закро", // закрой
  "закры", // закрыть/закрываю («закр» ловил бы «закрепи»)
  "убер", // убери
  "пауз", // пауза/на паузу (глагольная рамка «поставь на паузу» → mixed)
  // Аудит ядра [3]: частые синонимы остановки — «хватит искать катку» ловился neutral и НЕ
  // подавлял авто-реплей запускного навыка (тот же класс, что живой инцидент «прекрати поиск»).
  "хватит", // хватит (точный стем — не ловим «хватай/хватка»)
  "кончай", // кончай (точный — не «кончил/конец/кончита»)
  "заканч", // заканчивай
  "stop",
  "cancel",
  "close",
  "quit",
  "disable",
  "halt",
  "pause",
];

/** Токены слова/числа, нижний регистр, ё→е (кириллический \b в regex не работает). */
function tokens(s: string): string[] {
  return s.toLowerCase().replace(/ё/g, "е").match(/[a-zа-я0-9]+/gu) ?? [];
}

function hasStem(toks: readonly string[], stems: readonly string[]): boolean {
  return toks.some((t) => stems.some((stem) => t.startsWith(stem)));
}

/** Полярность текста: start / stop / mixed (есть оба класса) / neutral (нет ни одного). */
export function intentPolarity(text: string): IntentPolarity {
  const toks = tokens(text);
  const start = hasStem(toks, START_STEMS);
  const stop = hasStem(toks, STOP_STEMS);
  if (start && stop) return "mixed";
  if (start) return "start";
  if (stop) return "stop";
  return "neutral";
}

/**
 * Конфликт полярности команды и триггера навыка (§8): строгий start↔stop в ОБЕ стороны.
 * mixed/neutral с любой стороны → НЕ конфликт (неоднозначность отдаём модели).
 */
export function polarityConflict(command: string, skillTrigger: string): boolean {
  const c = intentPolarity(command);
  if (c !== "start" && c !== "stop") return false;
  const s = intentPolarity(skillTrigger);
  return (c === "stop" && s === "start") || (c === "start" && s === "stop");
}
