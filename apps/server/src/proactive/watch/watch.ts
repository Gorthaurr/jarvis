/**
 * НАБЛЮДЕНИЕ (watch, §долгие-задачи) — durable повторяющаяся проверка условия с проактивным уведомлением.
 *
 * Гэп, который закрывает: `set_reminder` — ОДНОРАЗОВЫЙ таймер; `auto-predictor` — recurring, но ТОЛЬКО
 * торговый и не вызывается пользователем; `proactive/triggers|scheduler` — стабы. Не было generic «следи
 * за X, проверяй каждые N, скажи КОГДА Y». Watch это даёт: модель формулирует ЧТО смотреть и УСЛОВИЕ
 * на естественном языке; сервис периодически прогоняет проверку (инъектируемый checker — LLM+web/market),
 * и при выполнении условия проговаривает результат тем же каналом проактивной речи, что напоминания.
 *
 * Концепт: НЕ хардкодим источники. Checker — общий (LLM водит web_search/web_fetch/market_quote и сам
 * добывает текущее значение). Сервис не знает, ЧТО именно отслеживается — только «проверь и скажи, met ли».
 */

/** Состояние наблюдения. active — следим; fired — сработало (one-shot завершилось); cancelled — снято;
 *  suspended — приостановлено после серии провалов проверки (dead-watch, D3): не тикает, не воскресает
 *  на boot, владельцу сказано ОДИН раз. */
export type WatchStatus = "active" | "fired" | "cancelled" | "suspended";

/** Запись наблюдения (durable). Источник истины — `data/watches.json`. */
export interface Watch {
  id: string;
  /** Сессия-создатель (для доставки точному каналу; на reconnect догоняем по userId). */
  sessionId: string;
  /** Владелец — уведомление доставляется ТОЛЬКО ему (как у напоминаний, §6B/B3). */
  userId: string;
  /** ЧТО отслеживать, на естественном языке («курс биткоина», «заголовок статьи на example.com»). */
  what: string;
  /** УСЛОВИЕ уведомления, на естественном языке («упал ниже 60000», «появилось слово „продано"»). */
  condition: string;
  /**
   * §Волна3 (3.4): ЛОКАЛЬНЫЙ предикат — проверка НА КЛИЕНТЕ (словарь wait_for: окно/UIA/OCR-текст/
   * звук/GSI-пуш игры) за $0 и миллисекунды, БЕЗ LLM-чекера. «Скажи, когда матч найдётся» =
   * watch с предикатом, а задача агента завершается сразу после запуска поиска — класс «долгое
   * ожидание события» уходит из agent-петли целиком. Форма — WaitCondition из @jarvis/protocol
   * (храним как unknown: watch-слой не тянет типы протокола, валидирует клиент).
   */
  predicate?: unknown;
  /** Период проверки (мс). Клампится сервисом к [minIntervalMs, …]. */
  intervalMs: number;
  /** true — продолжать следить и после первого срабатывания (отслеживание-непрерывно, антидребезг по
   *  summary); false — one-shot (после первого met → fired, снимаем). */
  continuous: boolean;
  status: WatchStatus;
  createdAt: number;
  /** Когда последний раз проверяли (для расчёта следующего due). */
  lastCheckAt?: number;
  /** Последнее наблюдённое значение (контекст для дельты/логов/следующей проверки). */
  lastValue?: string;
  /**
   * Ревью фиксов, 2-й проход (R4), только gsi+gone: когда источник последний раз наблюдался ЖИВЫМ
   * (fresh) в рамках ЭТОГО наблюдения. Видели живым → потом stale = исчез, независимо от того, попал
   * ли редкий тик в короткое окно recentlyGone клиента (иначе интервал > окна давал «лотерею»:
   * one-shot gone-watch мог молчать навсегда). Durable — переживает рестарт сервера.
   */
  sawFreshAt?: number;
  /** Когда условие сработало (для one-shot/телеметрии). */
  firedAt?: number;
  /** Антидребезг: о чём уже уведомили (в continuous не дублируем то же самое подряд). */
  lastNotifiedSummary?: string;
  /** Сработало, но активной озвучки не было (приложение закрыто) → проговорить при следующем подключении. */
  pendingNotify?: string;
  /** Dead-watch (D3, форензика 2026-07-14): подряд провалившихся проверок. ≥порога → suspended + ОДНО
   *  честное уведомление владельцу (раньше 142 провала подряд горели в тишине). Сбрасывается на успехе. */
  consecutiveFailures?: number;
}

/** Итог одной проверки наблюдения. Чистая структура (checker её возвращает). */
export interface CheckResult {
  /** Выполнено ли условие ПРЯМО СЕЙЧАС. */
  met: boolean;
  /** Наблюдённое значение (для контекста/лога/дельты), напр. «61 200 USD». */
  value?: string;
  /** Фраза пользователю при срабатывании И строка для лога (по делу, без боллерплейта). */
  summary: string;
  /** Проверка не удалась (сеть/нет данных) — НЕ считаем met, логируем и пробуем в следующий тик. */
  error?: string;
  /** Ревью р2 #6: ошибка ТРАНЗИЕНТНАЯ (нет живой сессии клиента) — НЕ инкрементит dead-watch (клиент
   *  вернётся). Отличаем от неисполнимого условия (реальный провал, копится к suspend). */
  transient?: boolean;
}

/** Проверяльщик условия наблюдения. Инъектируется (LLM+web/market в server.ts; мок в тестах). */
export type WatchChecker = (w: Watch) => Promise<CheckResult>;

/** Время следующей проверки наблюдения: ещё не проверяли → сейчас (сразу базовый замер), иначе +интервал. */
export function dueAt(w: Watch, now: number): number {
  return w.lastCheckAt === undefined ? now : w.lastCheckAt + w.intervalMs;
}
