/**
 * AMBIENT-ОСВЕДОМЛЁННОСТЬ (§проактив-всё) — Джарвис САМ непрерывно и ДЁШЕВО следит за источниками жизни
 * владельца и проактивно сообщает важное: «Сэр, вам написал Герман», «Не забудьте оплатить счёт завтра».
 *
 * Архитектура (лучшее, не срез): движок ИСТОЧНИКО-АГНОСТИЧЕН. Источник (AmbientSource) ДЁШЕВО (без LLM)
 * отдаёт текущие сигналы; движок дедуплицирует (не повторяет), фильтрует по САЛИЕНТНОСТИ, и проговаривает
 * новое важное тем же проактивным каналом, что напоминания (уважает «не мешать» §9). LLM зовётся ТОЛЬКО
 * чтобы СФОРМУЛИРОВАТЬ новое важное событие (редко) — токен-эконом это КАЧЕСТВО, а не дешёвый срез.
 */

/** Обнаруженное ambient-событие. key — стабильный идентификатор для дедупа (одно событие — одно уведомление). */
export interface AmbientSignal {
  /** id источника (telegram/obligations/…). */
  sourceId: string;
  /** Владелец (доставка только ему, §6B/B3). */
  userId: string;
  /** Стабильный ключ дедупа: повтор того же key — НЕ уведомляем второй раз. */
  key: string;
  /** Суть для пользователя (NL), напр. «Герман написал в Telegram». */
  title: string;
  /** Доп. контекст (текст сообщения / сумма и дата счёта) — для фразировки и лога. */
  detail?: string;
  /** 0..1 — насколько это важно прерывать владельца (порог в движке). */
  salience: number;
  /** Срочное — проговаривается даже когда пользователь занят (§9 urgent). Иначе ждёт свободного канала. */
  urgent?: boolean;
  /** Когда обнаружено. */
  ts: number;
}

/** Источник ambient-сигналов. poll() — ДЁШЕВО (в идеале без LLM): текущие сигналы владельца. */
export interface AmbientSource {
  /** Стабильный id (для seen-store и логов). */
  id: string;
  /** Человекочитаемая метка (для статуса). */
  label: string;
  /** Включён ли источник (рантайм-тумблер). */
  enabled(): boolean;
  /** Текущие сигналы (новые движок отфильтрует по seen-store). Ошибка/недоступность → []. */
  poll(): Promise<AmbientSignal[]>;
}

/**
 * Опциональный фразировщик: превратить сырой сигнал в живую дворецкую фразу. Зовётся ТОЛЬКО на НОВЫЙ
 * салиентный сигнал (редко) — не на каждый тик. null → берём signal.title как есть (без LLM).
 */
export type AmbientPhraser = (s: AmbientSignal) => Promise<string | null>;
