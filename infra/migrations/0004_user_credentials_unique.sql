-- =============================================================================
-- §13 / Фаза 6B (B4) — миграция 0004_user_credentials_unique
-- =============================================================================
-- user_credentials (из 0001) хранит per-user шифрованные ключи интеграций. Для upsert по
-- (user_id, service) — «один ключ на (юзер × сервис)» — нужен UNIQUE-индекс (ON CONFLICT).
-- Идемпотентно, PGlite-совместимо. НЕ трогаем 0001.
-- =============================================================================
CREATE UNIQUE INDEX IF NOT EXISTS uq_user_credentials_user_service
    ON user_credentials (user_id, service);
